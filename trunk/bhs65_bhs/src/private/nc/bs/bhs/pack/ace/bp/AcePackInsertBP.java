package nc.bs.bhs.pack.ace.bp;

import nc.bs.bhs.pack.rule.ReWriteSaleOrderRule;
import nc.bs.bhs.pack.plugin.bpplugin.PackPluginPoint;
import nc.impl.pubapp.pattern.data.bill.template.InsertBPTemplate;
import nc.impl.pubapp.pattern.rule.IRule;
import nc.impl.pubapp.pattern.rule.processer.AroundProcesser;
import nc.vo.bhs.pack.AggSoOrderPackVO;

/**
 * 标准单据新增BP
 */
public class AcePackInsertBP {

	public AggSoOrderPackVO[] insert(AggSoOrderPackVO[] bills) {

		InsertBPTemplate<AggSoOrderPackVO> bp = new InsertBPTemplate<AggSoOrderPackVO>(
				PackPluginPoint.INSERT);
		this.addBeforeRule(bp.getAroundProcesser());
		this.addAfterRule(bp.getAroundProcesser());
		return bp.insert(bills);

	}

	/**
	 * 新增后规则
	 * 
	 * @param processor
	 */
	private void addAfterRule(AroundProcesser<AggSoOrderPackVO> processor) {
		// TODO 新增后规则
		IRule<AggSoOrderPackVO> rule = null;
		rule = new nc.bs.pubapp.pub.rule.BillCodeCheckRule();
		((nc.bs.pubapp.pub.rule.BillCodeCheckRule) rule).setCbilltype("PACK");
		((nc.bs.pubapp.pub.rule.BillCodeCheckRule) rule)
				.setCodeItem("vbillno");
		((nc.bs.pubapp.pub.rule.BillCodeCheckRule) rule)
				.setGroupItem("pk_group");
		((nc.bs.pubapp.pub.rule.BillCodeCheckRule) rule).setOrgItem("pk_org");
		processor.addAfterRule(rule);
		IRule<AggSoOrderPackVO> rule1 = new ReWriteSaleOrderRule();
		processor.addAfterRule(rule1);
	}

	/**
	 * 新增前规则
	 * 
	 * @param processor
	 */
	private void addBeforeRule(AroundProcesser<AggSoOrderPackVO> processer) {
		// TODO 新增前规则
		IRule<AggSoOrderPackVO> rule = null;
		rule = new nc.bs.pubapp.pub.rule.FillInsertDataRule();
		processer.addBeforeRule(rule);
		rule = new nc.bs.pubapp.pub.rule.CreateBillCodeRule();
		((nc.bs.pubapp.pub.rule.CreateBillCodeRule) rule).setCbilltype("PACK");
		((nc.bs.pubapp.pub.rule.CreateBillCodeRule) rule)
				.setCodeItem("vbillno");
		((nc.bs.pubapp.pub.rule.CreateBillCodeRule) rule)
				.setGroupItem("pk_group");
		((nc.bs.pubapp.pub.rule.CreateBillCodeRule) rule).setOrgItem("pk_org");
		processer.addBeforeRule(rule);
	}
}
